"""Обучение YOLOv12 на датасете радара (2 класса: player_me, enemy).

Запуск:
    # Competitive / 1 класс (старый датасет de_dust2):
    python -m dataset_tools.trainer --map de_dust2 --nc 1

    # Wingman / 2 класса (новый датасет de_poseidon):
    python -m dataset_tools.trainer --map de_poseidon
    python -m dataset_tools.trainer --map de_poseidon --model yolo12n --epochs 80

Что делает:
    1. Проверяет наличие датасета в dataset_tools/dataset/{map_name}/
    2. Создаёт train/val разбивку (80/20)
    3. Генерирует dataset.yaml (nc=2 по умолчанию)
    4. Запускает YOLO.train()
    5. Копирует best.pt в radar_position/weights/{map_name}_best.pt

Классы по умолчанию (nc=2):
    0 = player_me  (бордовый/жёлтый значок игрока)
    1 = enemy      (красный ромб, красный "?")
"""

from __future__ import annotations

import argparse
import logging
import random
import shutil
import sys
from pathlib import Path

_LOG = logging.getLogger("trainer")


def prepare_split(
    dataset_dir: Path,
    val_ratio:   float = 0.2,
    seed:        int   = 42,
) -> tuple[list[Path], list[Path]]:
    """Разбивает изображения на train/val.

    Returns:
        (train_images, val_images)
    """
    images = sorted((dataset_dir / "images").glob("*.png"))
    if not images:
        raise FileNotFoundError(
            f"Изображения не найдены в {dataset_dir / 'images'}\n"
            "Сначала запусти: python -m dataset_tools.collector --map de_dust2"
        )

    random.seed(seed)
    shuffled = images[:]
    random.shuffle(shuffled)

    split = int(len(shuffled) * (1 - val_ratio))
    return shuffled[:split], shuffled[split:]


def write_split_files(
    dataset_dir: Path,
    train: list[Path],
    val:   list[Path],
) -> tuple[Path, Path]:
    """Записывает train.txt и val.txt с путями к изображениям."""
    train_txt = dataset_dir / "train.txt"
    val_txt   = dataset_dir / "val.txt"

    train_txt.write_text(
        "\n".join(str(p.resolve()) for p in train), encoding="utf-8"
    )
    val_txt.write_text(
        "\n".join(str(p.resolve()) for p in val), encoding="utf-8"
    )
    return train_txt, val_txt


_DEFAULT_NAMES: dict[int, dict[int, str]] = {
    1: {0: "player_dot"},
    2: {0: "player_me", 1: "enemy"},
}


def write_yaml(
    yaml_path:  Path,
    train_txt:  Path,
    val_txt:    Path,
    map_name:   str,
    nc:         int = 2,
    names:      dict[int, str] | None = None,
) -> None:
    """Генерирует dataset.yaml для ultralytics."""
    if names is None:
        names = _DEFAULT_NAMES.get(nc, {i: f"class_{i}" for i in range(nc)})

    names_str = "\n".join(f"  {k}: {v}" for k, v in sorted(names.items()))
    content = (
        f"# Auto-generated by dataset_tools/trainer.py\n"
        f"# Map: {map_name}\n\n"
        f"train: {train_txt.resolve()}\n"
        f"val:   {val_txt.resolve()}\n\n"
        f"nc: {nc}\n"
        f"names:\n"
        f"{names_str}\n"
    )
    yaml_path.write_text(content, encoding="utf-8")
    _LOG.info("dataset.yaml (nc=%d): %s", nc, yaml_path)


def train(
    map_name:    str   = "de_dust2",
    model_arch:  str   = "yolo12n",
    epochs:      int   = 50,
    img_size:    int   = 320,
    batch:       int   = 32,
    nc:          int   = 2,
    dataset_dir: Path | None = None,
    out_dir:     Path | None = None,
) -> Path:
    """Запускает обучение YOLO.

    Returns:
        Путь к best.pt скопированному в radar_position/weights/
    """
    try:
        from ultralytics import YOLO
    except ImportError as exc:
        raise ImportError("Установи ultralytics: pip install ultralytics") from exc

    root = Path(__file__).resolve().parent

    if dataset_dir is None:
        dataset_dir = root / "dataset" / map_name

    if out_dir is None:
        out_dir = root / "runs" / map_name

    _LOG.info("=== Обучение YOLOv12 ===")
    _LOG.info("Карта:    %s", map_name)
    _LOG.info("Модель:   %s", model_arch)
    _LOG.info("Датасет:  %s", dataset_dir)

    # --- Подготовка разбивки ---
    train_imgs, val_imgs = prepare_split(dataset_dir)
    _LOG.info("Train: %d | Val: %d", len(train_imgs), len(val_imgs))

    train_txt, val_txt = write_split_files(dataset_dir, train_imgs, val_imgs)

    yaml_path = dataset_dir / "dataset.yaml"
    write_yaml(yaml_path, train_txt, val_txt, map_name, nc=nc)

    # --- Загрузка модели ---
    # yolo12n.pt / yolo12s.pt / yolo12m.pt — скачивается автоматически
    # Если нет интернета — укажи локальный путь
    pt_name = f"{model_arch}.pt"
    _LOG.info("Загрузка базовой модели: %s (скачается если нет локально)", pt_name)
    model = YOLO(pt_name)

    # --- Обучение ---
    results = model.train(
        data=str(yaml_path),
        epochs=epochs,
        imgsz=img_size,
        batch=batch,
        project=str(out_dir),
        name="train",
        exist_ok=True,
        verbose=True,
    )

    # --- Копируем best.pt в weights/ ---
    best_src = out_dir / "train" / "weights" / "best.pt"
    weights_dir = (
        Path(__file__).resolve().parents[1]
        / "radar_position"
        / "weights"
    )
    weights_dir.mkdir(parents=True, exist_ok=True)
    best_dst = weights_dir / f"{map_name}_best.pt"

    if best_src.is_file():
        shutil.copy2(best_src, best_dst)
        _LOG.info("Веса скопированы: %s → %s", best_src.name, best_dst)
    else:
        _LOG.warning("best.pt не найден в %s", best_src)

    return best_dst


def main() -> int:
    logging.basicConfig(level=logging.INFO, format="%(message)s")

    parser = argparse.ArgumentParser(description="Train YOLOv12 on radar dataset")
    parser.add_argument("--map",    default="de_dust2")
    parser.add_argument("--model",  default="yolo12n",
                        help="Архитектура: yolo12n / yolo12s / yolo12m")
    parser.add_argument("--epochs", type=int, default=50)
    parser.add_argument("--imgsz",  type=int, default=320)
    parser.add_argument("--batch",  type=int, default=32)
    parser.add_argument(
        "--nc", type=int, default=2,
        help="Кол-во классов: 1 (старый датасет player_dot) или 2 (player_me + enemy). По умолч. 2",
    )
    parser.add_argument("--dataset", default=None,
                        help="Путь к папке датасета (по умолч. dataset_tools/dataset/{map})")
    args = parser.parse_args()

    train(
        map_name=args.map,
        model_arch=args.model,
        epochs=args.epochs,
        img_size=args.imgsz,
        batch=args.batch,
        nc=args.nc,
        dataset_dir=Path(args.dataset) if args.dataset else None,
    )
    return 0


if __name__ == "__main__":
    sys.exit(main())
